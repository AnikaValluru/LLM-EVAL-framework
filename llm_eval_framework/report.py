"""
Report generator — produces a clean Markdown eval report from results.
"""

from pathlib import Path
from datetime import datetime


def generate_report(results: dict, output_dir: str = "results") -> str:
    """
    Generate a Markdown report from eval results.

    Args:
        results: Full results dict from eval_runner.run_eval()
        output_dir: Directory to write the report.

    Returns:
        Path to the generated report file.
    """
    summary = results["summary"]
    cases = results["results"]

    suite_name = summary["suite_name"]
    model = summary["model"]
    timestamp = summary.get("timestamp", datetime.now().isoformat())
    total = summary["total"]
    passed = summary["passed"]
    failed = summary["failed"]
    pass_rate = summary["pass_rate"] * 100
    avg_score = summary["avg_score"]

    lines = []

    # Header
    lines.append(f"# Eval Report: {suite_name}")
    lines.append(f"")
    lines.append(f"| | |")
    lines.append(f"|---|---|")
    lines.append(f"| **Model** | `{model}` |")
    lines.append(f"| **Run at** | {timestamp} |")
    lines.append(f"| **Total cases** | {total} |")
    lines.append(f"| **Passed** | {passed} ✅ |")
    lines.append(f"| **Failed** | {failed} ❌ |")
    lines.append(f"| **Pass rate** | {pass_rate:.1f}% |")
    lines.append(f"| **Avg score** | {avg_score:.2f} |")
    lines.append("")

    # Pass/fail bar
    bar_width = 40
    filled = int(pass_rate / 100 * bar_width)
    bar = "█" * filled + "░" * (bar_width - filled)
    lines.append(f"**Progress:** `[{bar}]` {pass_rate:.1f}%")
    lines.append("")

    # Per-case results
    lines.append("## Test Case Results")
    lines.append("")

    for case in cases:
        status = "✅ PASS" if case["passed"] else "❌ FAIL"
        score = case["score_result"]["score"]
        lines.append(f"### {status} `{case['id']}` — {case['description']}")
        lines.append(f"")
        lines.append(f"**Score:** {score:.2f} ({case['score_result']['checks_passed']}/{case['score_result']['checks_total']} checks)")
        lines.append("")
        lines.append(f"**Input:**")
        lines.append(f"```")
        lines.append(case["input"])
        lines.append(f"```")
        lines.append("")
        lines.append(f"**Output:**")
        lines.append(f"```")
        # Truncate very long outputs
        output_display = case["output"]
        if len(output_display) > 500:
            output_display = output_display[:500] + "\n... [truncated]"
        lines.append(output_display)
        lines.append(f"```")
        lines.append("")

        # Check details
        checks = case["score_result"].get("check_details", {})
        if checks:
            lines.append("**Check details:**")
            lines.append("")
            lines.append("| Check | Result | Message |")
            lines.append("|---|---|---|")
            for check_name, detail in checks.items():
                icon = "✅" if detail["passed"] else "❌"
                lines.append(f"| `{check_name}` | {icon} | {detail['message']} |")
            lines.append("")

        lines.append("---")
        lines.append("")

    # Footer
    lines.append(f"*Generated by LLM Eval Framework — {timestamp}*")

    report_content = "\n".join(lines)

    Path(output_dir).mkdir(exist_ok=True)
    safe_name = suite_name.lower().replace(" ", "_")
    timestamp_str = datetime.now().strftime("%Y%m%d_%H%M%S")
    report_path = f"{output_dir}/{safe_name}_{timestamp_str}_report.md"

    with open(report_path, "w") as f:
        f.write(report_content)

    return report_path
